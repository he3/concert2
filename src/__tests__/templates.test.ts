// 10. Workflow-registry consistency
// ======================================================================

describe('workflow-registry consistency', () => {
  const registryPath = path.join(ROOT, 'docs', 'concert', 'stage-registry.jsonc');
  const content = fs.readFileSync(registryPath, 'utf-8');
  const registry = jsonc.parse(content);
  const stageNames = new Set(registry.stages.map((s: { name: string }) => s.name));

  it('all workflow variants reference valid stage names', () => {
    for (const [variant, stages] of Object.entries(registry.workflows)) {
      for (const stage of stages as string[]) {
        expect(
          stageNames.has(stage),
          `Workflow "${variant}" references unknown stage "${stage}"`
        ).toBe(true);
      }
    }
  });

  const workflowFiles = [
    { name: 'mission-full', file: 'CONCERT-WORKFLOW-MISSION-FULL.md' },
    { name: 'mission-medium', file: 'CONCERT-WORKFLOW-MISSION-MEDIUM.md' },
    { name: 'mission-small', file: 'CONCERT-WORKFLOW-MISSION-SMALL.md' },
  ];

  for (const { name, file } of workflowFiles) {
    it(`${file} Stages line matches registry workflows.${name}`, () => {
      const workflowPath = path.join(ROOT, 'docs', 'concert', 'workflows', file);
      expect(fs.existsSync(workflowPath)).toBe(true);
      const workflowContent = fs.readFileSync(workflowPath, 'utf-8');
      const match = workflowContent.match(/^Stages:\s*(.+)$/m);
      expect(match, `No "Stages:" line found in ${file}`).not.toBeNull();
      const fileStages = match![1].split(',').map((s: string) => s.trim());
      const registryStages = registry.workflows[name];
      expect(fileStages, `${file} stages don't match registry`).toEqual(registryStages);
    });
  }
});

