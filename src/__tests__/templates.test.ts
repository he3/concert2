// ======================================================================
// 1. Template files (defaults for init)
// ======================================================================

describe('template files', () => {
  it('concert.jsonc is parseable', () => {
    const configPath = path.join(TEMPLATES_DIR, 'concert.jsonc');
    expect(fs.existsSync(configPath)).toBe(true);
    const content = fs.readFileSync(configPath, 'utf-8');
    const errors: jsonc.ParseError[] = [];
    jsonc.parse(content, errors);
    expect(errors).toHaveLength(0);
  });

  it('state.json is valid JSON with required fields', () => {
    const statePath = path.join(TEMPLATES_DIR, 'docs', 'concert', 'state.json');
    expect(fs.existsSync(statePath)).toBe(true);
    const state = JSON.parse(fs.readFileSync(statePath, 'utf-8'));
    for (const field of [
      'mission',
      'branch',
      'pr_number',
      'stage',
      'pipeline',
      'phases_completed',
      'tasks_completed',
      'commits',
      'cost',
      'blockers',
      'telemetry',
      'failure_log',
      'history',
      'next_steps',
    ]) {
      expect(state, `Missing: ${field}`).toHaveProperty(field);
    }
  });

  it('CLAUDE.md has CONCERT:START and CONCERT:END markers', () => {
    const claudePath = path.join(TEMPLATES_DIR, 'CLAUDE.md');
    expect(fs.existsSync(claudePath)).toBe(true);
    const content = fs.readFileSync(claudePath, 'utf-8');
    expect(content).toContain('CONCERT:START');
    expect(content).toContain('CONCERT:END');
  });

  it('docs/concert/README.md exists', () => {
    expect(fs.existsSync(path.join(TEMPLATES_DIR, 'docs', 'concert', 'README.md'))).toBe(true);
  });

  it('docs/concert/missions/.gitkeep exists', () => {
    expect(fs.existsSync(path.join(TEMPLATES_DIR, 'docs', 'concert', 'missions', '.gitkeep'))).toBe(
      true
    );
  });

  it('docs/concert/TODO.md exists with ad hoc description', () => {
    const todoPath = path.join(TEMPLATES_DIR, 'docs', 'concert', 'TODO.md');
    expect(fs.existsSync(todoPath)).toBe(true);
    const content = fs.readFileSync(todoPath, 'utf-8');
    expect(content).toContain('TODO');
    expect(content).toContain('should NOT');
  });

  it('docs/concert/IDEAS.md exists with ad hoc description', () => {
    const ideasPath = path.join(TEMPLATES_DIR, 'docs', 'concert', 'IDEAS.md');
    expect(fs.existsSync(ideasPath)).toBe(true);
    const content = fs.readFileSync(ideasPath, 'utf-8');
    expect(content).toContain('Ideas');
    expect(content).toContain('should NOT');
  });

  it('no file in templates exceeds 50KB', () => {
    function checkSize(dir: string): void {
      if (!fs.existsSync(dir)) return;
      const entries = fs.readdirSync(dir, { withFileTypes: true });
      for (const entry of entries) {
        const fullPath = path.join(dir, entry.name);
        if (entry.isDirectory()) {
          checkSize(fullPath);
        } else {
          const stats = fs.statSync(fullPath);
          expect(stats.size, `${fullPath} exceeds 50KB`).toBeLessThan(50 * 1024);
        }
      }
    }
    checkSize(TEMPLATES_DIR);
  });
});

